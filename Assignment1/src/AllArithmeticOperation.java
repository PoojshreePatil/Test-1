
public class AllArithmeticOperation {

	public static void main(String[] args) {
		int a = 3, b = 5, c;
		c = a + b;
		System.out.println("The Addition of a and b is : " + c);
		c = a - b;
		System.out.println("The Substraction a and b is : " + c);
		c = a * b;
		System.out.println("The Multiplicatio of a and b is : " + c);
		c = a / b;
		System.out.println("The Division of a and b is : " + c);
	}

}
